package org.jan;

public class Patternfinal {
	public static void main(String[] args) {
		for (int j=0;j<=5;j++) {
			
			for (int k=0;k<j;k++) {
				System.out.print("* ");
				
			}System.out.print("\n");
			
		}
		for (int j=1;j<=8;j++) {
			
			for (int k=1;k<j;k++) {
				System.out.print(k+" ");
				
			}System.out.print("\n");
			
		}
	}}
